package manipulação;

public class inferencia {

	public static void main(String[] args) {
		var b = "ola";
		double c; //declaração da variavel
		c = 5; // variavel iniciada
		var j = 5;
		int  k;
		k = 7;
		var d = 4.5;
		var l = 9;
		
		System.out.println(b);
		System.out.println(c); //variavel usada
		System.out.println(d);
		System.out.println(k);
		System.out.println(j);
		System.out.println(l);
		
		//quando uma variavel é declarada, retirasse o var, ja que ela ja vai ser inicializada direto
	}
}
